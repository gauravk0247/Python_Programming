# Create a list using []
a = [1,4,7,9,6]

# Print the list using print() function
print(a)

# print(a[5]) #list out of range

# Access using index using a[0], a[1], a[2]
print(a[2])

# Change the value of list using this
a[0] = 23
print(a)

# We can create a list with items of different types
c = [45, "Gaurav", False, 8.6]
print(c)

# List slicing
friends = ["Gaurav", "Ritik", "Pratik", "Rakesh","Monaco", 34]
print(friends[0:4])
print(friends[-5:])